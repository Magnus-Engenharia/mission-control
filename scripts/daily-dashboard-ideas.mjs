#!/usr/bin/env node
import fs from 'fs';
import path from 'path';
import Database from 'better-sqlite3';

const DB_PATH = '/Users/magnuseng/Projects/mission-control/mission-control.db';

const PROJECT_FEATURE_FILES = ['PROJECT_CRITICALS.md', 'PROJECT_FEATURES.md', 'FEATURES.md', 'FEATURES.txt', 'features.md', 'features.txt'];

function normalizeText(v) {
  return String(v || '').toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '');
}

function extractFeatureSignals(filePath) {
  try {
    const raw = fs.readFileSync(filePath, 'utf8');
    const lines = raw
      .split(/\r?\n/)
      .map((l) => l.trim())
      .filter((l) => l);
    const bullets = lines
      .filter((l) => /^[-*•]|^(\d+\.)/.test(l))
      .map((l) => l.replace(/^[-*•]|^\d+\./, '').trim())
      .filter(Boolean);
    return bullets.length ? bullets : lines.filter((l) => l.length > 5);
  } catch {
    return [];
  }
}

function loadProjectFeatureText(repoPath) {
  for (const filename of PROJECT_FEATURE_FILES) {
    const fpath = path.join(repoPath, filename);
    if (!fs.existsSync(fpath)) continue;
    const extracted = extractFeatureSignals(fpath);
    if (extracted.length) return extracted.join('\n');
  }
  return '';
}

function hasOverlap(featureText, ideaTitle) {
  const normFeatures = normalizeText(featureText || '');
  const normIdea = normalizeText(ideaTitle);
  return normFeatures.includes(normIdea.replace(/[^a-z0-9]+/g, ' '));
}

const improvementIdeas = [
  {
    titleSuffix: 'Melhoria de UX no fluxo principal',
    summary: 'Revisar os pontos de fricção na navegação e reduzir cliques para ações críticas.',
    tags: ['ux', 'produto'],
    score: 8,
  },
  {
    titleSuffix: 'Refatoração de performance no backend',
    summary: 'Analisar querys lentas e cache de leitura para reduzir tempo de resposta em endpoints de alto uso.',
    tags: ['backend', 'performance', 'refactor'],
    score: 7,
  },
  {
    titleSuffix: 'Automatização de qualidade/CI',
    summary: 'Adicionar pipelines de lint/testes e validações para reduzir bugs em entrega contínua.',
    tags: ['devex', 'ci', 'qualidade'],
    score: 7,
  },
  {
    titleSuffix: 'Melhorar observabilidade',
    summary: 'Adicionar logs estruturados e dashboards para rastrear erros críticos e gargalos.',
    tags: ['monitoring', 'ops'],
    score: 6,
  },
  {
    titleSuffix: 'Evolução de features de retenção',
    summary: 'Adicionar fluxo simples de onboarding/retenção para aumentar uso recorrente do produto.',
    tags: ['negocio', 'produto'],
    score: 8,
  },
  {
    titleSuffix: 'Refinamento de acessibilidade',
    summary: 'Auditar contraste, navegação por teclado e labels para cumprir acessibilidade básica.',
    tags: ['frontend', 'acessibilidade', 'a11y'],
    score: 7,
  },
  {
    titleSuffix: 'Hardening de segurança',
    summary: 'Revisar autenticação e validações críticas para reduzir superfície de exposição.',
    tags: ['seguranca', 'backend'],
    score: 8,
  },
  {
    titleSuffix: 'Polish visual do dashboard',
    summary: 'Padronizar espaçamentos, contraste e estados vazios para experiência mais consistente.',
    tags: ['frontend', 'design'],
    score: 6,
  },
];

const now = new Date().toISOString();
const db = new Database(DB_PATH, { readonly: false });

try {
  const workspaces = db.prepare('SELECT id, name FROM workspaces').all();
  const projectsByWorkspace = db
    .prepare('SELECT id, name, repo_path FROM projects WHERE workspace_id = ? AND is_active = 1 ORDER BY created_at ASC');

  const existingIdea = db.prepare(
    "SELECT 1 FROM ideas WHERE workspace_id = ? AND project_id = ? AND title = ? LIMIT 1"
  );
  const insertIdea = db.prepare(
    `INSERT INTO ideas (id, workspace_id, title, summary, source, tags_json, project_id, is_new_project, status, score, created_at, updated_at)
     VALUES (?, ?, ?, ?, ?, ?, ?, 0, 'new', ?, ?, ?)`
  );

  let inserted = 0;

  for (const ws of workspaces) {
    const projects = projectsByWorkspace.all(ws.id);
    for (const project of projects) {
      const projectFeatures = loadProjectFeatureText(project.repo_path || '');
      let chosen = improvementIdeas.filter((idea) => !hasOverlap(projectFeatures, idea.titleSuffix));
      if (!chosen.length) {
        // fallback to all ideas when feature doc is too narrow
        chosen.push(...improvementIdeas);
      }
      // rotate candidates for some variety by project name hash-ish
      const seed = (project.name || '').length % chosen.length;
      for (let i = 0; i < Math.min(5, chosen.length); i++) {
        const idea = chosen[(seed + i) % chosen.length];
        const title = `${project.name} - ${idea.titleSuffix}`;

        if (existingIdea.get(ws.id, project.id, title)) {
          continue;
        }

        const params = [
          crypto.randomUUID(),
          ws.id,
          title,
          `${idea.summary} Aplicação-alvo: ${project.name}.`,
          'daily-autogenerated',
          JSON.stringify(idea.tags),
          project.id,
          idea.score,
          now,
          now
        ];
        insertIdea.run(...params);
        inserted += 1;
      }
    }
  }

  console.log(`daily-dashboard-ideas inserted=${inserted}`);
} catch (err) {
  console.error('daily-dashboard-ideas failed', err);
  process.exit(1);
} finally {
  db.close();
}
